#for loop on arrays:
numbers = [1, 2, 3, 4, 5]

# Initialize an empty list to store the squares
squares = []

# For loop to iterate through each number in the list
for number in numbers:
    # Calculate the square of the number
    square = number ** 2
    # Append the square to the list of squares
    squares.append(square)

# Output variable: the list of squares
print(squares)













